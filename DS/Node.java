//Class to create a binary tree
/**
 * @author Arushi --Node class for tree
 *
 */
public class Node {
	int data;
	Node left;
	Node right;

	// constructor calling
	Node() {
		data = 0;
		left = null;
		right = null;
	}

	// Parameterized constructor calling
	Node(int element) {
		data = element;
		left = null;
		right = null;
	}

}
